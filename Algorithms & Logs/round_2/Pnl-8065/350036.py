# ─── Pure-Python NN inference (no numpy) ─────────────────────────────────────
def _relu(x: float) -> float:
    return x if x > 0.0 else 0.0

def _nn_forward(features: list) -> float:
    """
    Forward pass: 7 → 24 → 12 → 1 network.
    Returns predicted osmium mid-price deviation from OSMIUM_FAIR (3 ticks ahead).
    """
    N = 7
    # Normalise
    x = [(features[i] - _NORM[i]) / _NORM[N + i] for i in range(N)]

    # Layer 1: 7 → 24 ReLU
    h1 = [_relu(sum(x[i] * _W1[i][j] for i in range(N)) + _b1[j]) for j in range(24)]

    # Layer 2: 24 → 12 ReLU
    h2 = [_relu(sum(h1[i] * _W2[i][j] for i in range(24)) + _b2[j]) for j in range(12)]

    # Layer 3: 12 → 1 linear
    out = sum(h2[i] * _W3[i][0] for i in range(12)) + _b3[0]

    # De-normalise
    return out * _NORM[15] + _NORM[14]


# ─── Trader ───────────────────────────────────────────────────────────────────
class Trader:
    def __init__(self):
        self._ema_osm: float  = float(OSMIUM_FAIR)
        self._mid_hist: list  = []          # rolling window (max 20)
        self._alpha: float    = 0.15        # EMA smoothing

    # ── rolling state ─────────────────────────────────────────────────────────
    def _update(self, mid: float) -> None:
        self._ema_osm = self._alpha * mid + (1.0 - self._alpha) * self._ema_osm
        self._mid_hist.append(mid)
        if len(self._mid_hist) > 20:
            self._mid_hist.pop(0)

    def _ma(self, n: int) -> float:
        w = self._mid_hist[-n:]
        return sum(w) / len(w) if w else float(OSMIUM_FAIR)

    def _vol_imb(self, od: OrderDepth) -> float:
        bv = float(sum(od.buy_orders.values()))   if od.buy_orders  else 0.0
        av = float(sum(abs(v) for v in od.sell_orders.values())) if od.sell_orders else 0.0
        d  = bv + av
        return (bv - av) / d if d > 0 else 0.0

    def _nn_fair(self, od: OrderDepth) -> float:
        """
        Compute adaptive fair value blending static anchor, EMA and NN signal.
        """
        if not od.sell_orders or not od.buy_orders:
            return float(OSMIUM_FAIR)

        best_ask = min(od.sell_orders.keys())
        best_bid = max(od.buy_orders.keys())
        mid      = (best_ask + best_bid) / 2.0

        feats = [
            best_bid - OSMIUM_FAIR,            # bid_dev
            best_ask - OSMIUM_FAIR,            # ask_dev
            mid      - OSMIUM_FAIR,            # mid_dev
            float(best_ask - best_bid),        # spread
            self._vol_imb(od),                 # vol_imbalance
            self._ma(5)  - OSMIUM_FAIR,        # MA5 dev
            self._ma(20) - OSMIUM_FAIR,        # MA20 dev
        ]
        nn_dev = _nn_forward(feats)

        # Blend: 55% static fair  +  20% EMA  +  25% NN-adjusted
        return (0.55 * OSMIUM_FAIR
                + 0.20 * self._ema_osm
                + 0.25 * (OSMIUM_FAIR + nn_dev * 0.35))

    # ── main ──────────────────────────────────────────────────────────────────
    def run(self, state: TradingState):
        result: Dict[str, List[Order]] = {}

        # ── PEPPER: hold maximum long the entire round ────────────────────────
        # Trend: +~1000 XIREC/day guaranteed. Buy every available lot ASAP.
        if PEPPER in state.order_depths:
            od  = state.order_depths[PEPPER]
            pos = state.position.get(PEPPER, 0)
            cap = PEPPER_LIMIT - pos
            orders: List[Order] = []
            if cap > 0 and od.sell_orders:
                for price in sorted(od.sell_orders.keys()):
                    if cap <= 0:
                        break
                    qty = min(abs(od.sell_orders[price]), cap)
                    orders.append(Order(PEPPER, price, qty))
                    cap -= qty
            if orders:
                result[PEPPER] = orders

        # ── OSMIUM: NN-guided mean-reversion market maker ─────────────────────
        if OSMIUM in state.order_depths:
            od  = state.order_depths[OSMIUM]
            pos = state.position.get(OSMIUM, 0)

            if not od.sell_orders or not od.buy_orders:
                return result, 1, ""

            best_ask = min(od.sell_orders.keys())
            best_bid = max(od.buy_orders.keys())
            mid      = (best_ask + best_bid) / 2.0

            self._update(mid)
            fair = self._nn_fair(od)

            ask_vol = abs(od.sell_orders[best_ask])
            bid_vol = od.buy_orders[best_bid]

            buy_cap  = OSMIUM_LIMIT - pos
            sell_cap = OSMIUM_LIMIT + pos
            orders: List[Order] = []

            # ── STEP 1: MEAN-REVERSION UNWIND (frees capacity first) ──────────
            # Sell longs when bid is at or above fair – we already have profit,
            # reset capacity so we can re-enter on the NEXT dip.
            MV_SELL_THRESH = 0    # sell longs when bid >= FAIR + 0 (= at fair)
            MV_BUY_THRESH  = 2    # buy shorts when ask <= FAIR - 2

            if pos > 0 and best_bid >= fair + MV_SELL_THRESH:
                qty = min(bid_vol, sell_cap, pos)  # unwind entire long
                if qty > 0:
                    orders.append(Order(OSMIUM, best_bid, -qty))
                    pos      -= qty
                    sell_cap -= qty
                    buy_cap   = OSMIUM_LIMIT - pos

            if pos < 0 and best_ask <= fair - MV_BUY_THRESH:
                qty = min(ask_vol, buy_cap, abs(pos))  # unwind entire short
                if qty > 0:
                    orders.append(Order(OSMIUM, best_ask, qty))
                    pos     += qty
                    buy_cap -= qty
                    sell_cap = OSMIUM_LIMIT + pos

            # ── STEP 2: TAKER – capture clear mispricing ──────────────────────
            TAKER_THRESH = 2   # buy when ask ≤ fair-2; sell when bid ≥ fair+2
            buy_cap  = OSMIUM_LIMIT - pos
            sell_cap = OSMIUM_LIMIT + pos

            for price in sorted(od.sell_orders.keys()):
                if buy_cap <= 0:
                    break
                if price <= fair - TAKER_THRESH:
                    qty = min(abs(od.sell_orders[price]), buy_cap)
                    orders.append(Order(OSMIUM, price, qty))
                    buy_cap -= qty

            for price in sorted(od.buy_orders.keys(), reverse=True):
                if sell_cap <= 0:
                    break
                if price >= fair + TAKER_THRESH:
                    qty = min(od.buy_orders[price], sell_cap)
                    orders.append(Order(OSMIUM, price, -qty))
                    sell_cap -= qty

            # ── STEP 3: MAKER – passive spread capture ────────────────────────
            # Post at fair ± 4  (grid-search optimum vs ±3 in v1)
            MAKER_SPREAD = 4
            maker_bid = int(math.floor(fair - MAKER_SPREAD))
            maker_ask = int(math.ceil(fair  + MAKER_SPREAD))

            buy_cap  = OSMIUM_LIMIT - pos
            sell_cap = OSMIUM_LIMIT + pos

            if buy_cap > 0:
                orders.append(Order(OSMIUM, maker_bid, min(buy_cap, 15)))
            if sell_cap > 0:
                orders.append(Order(OSMIUM, maker_ask, -min(sell_cap, 15)))

            if orders:
                result[OSMIUM] = orders

        # ── XIREC bid = 1 (beats all passive 0-bidders for market access) ─────
        return result, 1, ""